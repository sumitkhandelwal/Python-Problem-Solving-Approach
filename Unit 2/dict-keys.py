emp = {"name" : "Sumit", "age": 30, "salary":20000, "company":"SPPU"}
print ("\nAll Keys values of a dictionary is :")
for i in emp.keys():
    print (i)
print ("\nAll Values of a dictionary is :")
for i in emp.values():
    print (i)
print ("\nAll Items values of a dictionary is :")
for i in emp.items():
    print (i)

